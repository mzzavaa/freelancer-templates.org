/**
 * Options for composition generation.
 *
 * These options control how compositions are generated
 * from template and theme registries.
 */
export interface CompositionGenerationOptions {
  /**
   * Default duration in frames for generated compositions.
   * Can be overridden by template-specific settings.
   */
  defaultDurationInFrames?: number;

  /**
   * Default FPS for generated compositions.
   * Can be overridden by template-specific settings.
   */
  defaultFps?: number;

  /**
   * Default width for generated compositions.
   * Can be overridden by template-specific settings.
   */
  defaultWidth?: number;

  /**
   * Default height for generated compositions.
   * Can be overridden by template-specific settings.
   */
  defaultHeight?: number;

  /**
   * Whether to skip orphaned templates (no implementation).
   * @default true
   */
  skipOrphaned?: boolean;

  /**
   * Whether to include legacy IDs for backward compatibility.
   * @default true
   */
  includeLegacyIds?: boolean;

  /**
   * Filter to specific template IDs.
   * If provided, only these templates will be processed.
   */
  templateFilter?: string[];

  /**
   * Filter to specific theme names.
   * If provided, only these themes will be used.
   */
  themeFilter?: string[];
}

/**
 * Result of composition generation.
 *
 * Contains the generated compositions and metadata about
 * the generation process.
 */
export interface CompositionGenerationResult {
  /**
   * Array of generated composition configurations.
   */
  compositions: CompositionConfig[];

  /**
   * Number of templates processed.
   */
  templatesProcessed: number;

  /**
   * Number of templates skipped (orphaned or filtered).
   */
  templatesSkipped: number;

  /**
   * Number of themes used.
   */
  themesUsed: number;

  /**
   * IDs of orphaned templates that were skipped.
   */
  orphanedTemplates: string[];

  /**
   * Any warnings generated during the process.
   */
  warnings: string[];
}

