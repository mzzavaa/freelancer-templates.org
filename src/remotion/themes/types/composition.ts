/**
 * Components used to generate a composition ID.
 *
 * A composition ID is generated from the combination of
 * template, theme, and layout names in PascalCase format.
 *
 * @see Requirements: 7.4-7.6, 18.1-18.3
 */
export interface CompositionIdComponents {
  /**
   * Template identifier (will be converted to PascalCase).
   * @example "testimonial" → "Testimonial"
   */
  templateId: string;

  /**
   * Theme name (will be converted to PascalCase).
   * @example "ocean" → "Ocean"
   */
  themeName: string;

  /**
   * Layout name (will be converted to PascalCase).
   * @example "centered" → "Centered"
   */
  layoutName: string;
}

/**
 * Result of composition ID generation.
 *
 * Contains the generated ID and any uniqueness suffix applied.
 */
export interface CompositionIdResult {
  /**
   * The generated composition ID.
   * @example "Testimonial-Ocean-Centered"
   */
  id: string;

  /**
   * The base ID before any uniqueness suffix was applied.
   * @example "Testimonial-Ocean-Centered"
   */
  baseId: string;

  /**
   * Numeric suffix applied for uniqueness (if any).
   * Undefined if no suffix was needed.
   * @example 2, 3, etc.
   */
  suffix?: number;

  /**
   * Whether a suffix was applied to ensure uniqueness.
   */
  isUnique: boolean;
}

/**
 * Options for composition ID generation.
 */
export interface CompositionIdOptions {
  /**
   * Set of already-used IDs to check for conflicts.
   */
  usedIds?: Set<string>;

  /**
   * Whether to automatically append a suffix for uniqueness.
   * @default true
   */
  ensureUnique?: boolean;

  /**
   * Custom separator between ID components.
   * @default "-"
   */
  separator?: string;
}

