// Utility Functions
// ═══════════════════════════════════════════════════════════════════════════

/**
 * Converts a string to PascalCase.
 *
 * Used for generating composition IDs from kebab-case
 * template IDs and theme names.
 *
 * @param str - The string to convert
 * @returns The string in PascalCase
 *
 * @example
 * toPascalCase("testimonial") // "Testimonial"
 * toPascalCase("case-study") // "CaseStudy"
 * toPascalCase("game-day-intro") // "GameDayIntro"
 */
export function toPascalCase(str: string): string {
  return str
    .split(/[-_\s]+/)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join("");
}

/**
 * Generates a composition ID from its components.
 *
 * Creates a PascalCase ID in the format "Template-Theme-Layout".
 * Optionally ensures uniqueness by appending a numeric suffix.
 *
 * @param components - The template, theme, and layout identifiers
 * @param options - Options for ID generation
 * @returns The generated ID result
 *
 * @see Requirements: 7.4-7.6, 18.1-18.3
 *
 * @example
 * generateCompositionId(
 *   { templateId: "testimonial", themeName: "ocean", layoutName: "centered" }
 * )
 * // { id: "Testimonial-Ocean-Centered", baseId: "Testimonial-Ocean-Centered", isUnique: true }
 */
export function generateCompositionId(
  components: CompositionIdComponents,
  options: CompositionIdOptions = {}
): CompositionIdResult {
  const { usedIds, ensureUnique = true, separator = "-" } = options;

  const templatePascal = toPascalCase(components.templateId);
  const themePascal = toPascalCase(components.themeName);
  const layoutPascal = toPascalCase(components.layoutName);

  const baseId = `${templatePascal}${separator}${themePascal}${separator}${layoutPascal}`;

  // If no uniqueness check needed or no used IDs provided
  if (!ensureUnique || !usedIds || !usedIds.has(baseId)) {
    return {
      id: baseId,
      baseId,
      isUnique: true,
    };
  }

  // Find a unique ID by appending a numeric suffix
  let suffix = 2;
  let candidateId = `${baseId}${separator}${suffix}`;

  while (usedIds.has(candidateId)) {
    suffix++;
    candidateId = `${baseId}${separator}${suffix}`;
  }

  return {
    id: candidateId,
    baseId,
    suffix,
    isUnique: true,
  };
}

/**
 * Parses a composition ID into its components.
 *
 * Extracts the template, theme, and layout from a composition ID.
 * Returns undefined if the ID format is invalid.
 *
 * @param id - The composition ID to parse
 * @returns The parsed components or undefined if invalid
 *
 * @example
 * parseCompositionId("Testimonial-Ocean-Centered")
 * // { templateId: "testimonial", themeName: "ocean", layoutName: "centered" }
 */
export function parseCompositionId(
  id: string
): CompositionIdComponents | undefined {
  const parts = id.split("-");

  // Must have at least 3 parts: Template-Theme-Layout
  if (parts.length < 3) {
    return undefined;
  }

  // Handle IDs with numeric suffix (e.g., "Testimonial-Ocean-Centered-2")
  const lastPart = parts[parts.length - 1];
  const hasNumericSuffix = /^\d+$/.test(lastPart);

  if (hasNumericSuffix && parts.length < 4) {
    return undefined;
  }

  const effectiveParts = hasNumericSuffix ? parts.slice(0, -1) : parts;

  // For now, assume simple format: Template-Theme-Layout
  // More complex templates with hyphens would need special handling
  if (effectiveParts.length === 3) {
    return {
      templateId: effectiveParts[0].toLowerCase(),
      themeName: effectiveParts[1].toLowerCase(),
      layoutName: effectiveParts[2].toLowerCase(),
    };
  }

  // Handle multi-word components (e.g., "CaseStudy-MaterialBlue-FullWidth")
  // This is a simplified parser - complex cases may need template registry lookup
  return {
    templateId: effectiveParts[0].toLowerCase(),
    themeName: effectiveParts[1].toLowerCase(),
    layoutName: effectiveParts.slice(2).join("-").toLowerCase(),
  };
}

/**
 * Validates a composition ID format.
 *
 * Checks that the ID follows the expected PascalCase format
 * with hyphen separators.
 *
 * @param id - The composition ID to validate
 * @returns True if the ID format is valid
 */
export function isValidCompositionId(id: string): boolean {
  // Must have at least 3 parts separated by hyphens
  const parts = id.split("-");
  if (parts.length < 3) {
    return false;
  }

  // Each part must start with an uppercase letter (PascalCase)
  // or be a numeric suffix
  return parts.every((part, index) => {
    // Allow numeric suffix as last part
    if (index === parts.length - 1 && /^\d+$/.test(part)) {
      return true;
    }
    // Must start with uppercase letter
    return /^[A-Z][a-zA-Z0-9]*$/.test(part);
  });
}
