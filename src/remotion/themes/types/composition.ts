/**
 * Composition Type Definitions
 *
 * Core type definitions for dynamically generated Remotion compositions.
 * These types define the structure of composition configs, ID generation,
 * and the relationship between templates, themes, and layouts.
 *
 * @module themes/types/composition
 * @see Requirements: 7.1-7.6, 9.1-9.3
 */

import type { ComponentType } from "react";
import type { z } from "zod";
import type { Theme } from "./theme";
import type { BrandKit } from "./brandkit";

/**
 * Composition configuration interface.
 *
 * A CompositionConfig represents a complete Remotion composition
 * configuration, combining a template with a specific theme and layout.
 * These configs are dynamically generated by the CompositionGenerator.
 *
 * @see Requirements: 7.1-7.6
 */
export interface CompositionConfig {
  // ── Identity ──────────────────────────────────────────────────────
  /**
   * Unique composition identifier in PascalCase format.
   * Generated from template, theme, and layout names.
   * @example "Testimonial-Ocean-Centered"
   * @example "CaseStudy-Dark-Split"
   */
  id: string;

  /**
   * Legacy composition ID for backward compatibility.
   * Used to maintain existing URL patterns after migration.
   * @see Requirements: 9.1-9.3
   */
  legacyId?: string;

  // ── Component ─────────────────────────────────────────────────────
  /**
   * React component that renders this composition.
   * This is the template component with theme and layout pre-configured.
   */
  component: ComponentType<CompositionProps>;

  // ── Dimensions ────────────────────────────────────────────────────
  /**
   * Video width in pixels.
   */
  width: number;

  /**
   * Video height in pixels.
   */
  height: number;

  /**
   * Frames per second.
   */
  fps: number;

  /**
   * Total duration in frames.
   */
  durationInFrames: number;

  // ── Props ─────────────────────────────────────────────────────────
  /**
   * Default props passed to the composition component.
   * Contains the theme, layout, spec data, and optional BrandKit.
   */
  defaultProps: CompositionDefaultProps;

  // ── Schema ────────────────────────────────────────────────────────
  /**
   * Zod schema for Remotion Studio prop editing.
   * Enables type-safe prop editing in the Remotion Studio UI.
   */
  schema?: z.ZodType<CompositionProps>;

  // ── Metadata ──────────────────────────────────────────────────────
  /**
   * ID of the template this composition is based on.
   * @example "testimonial", "case-study"
   */
  templateId: string;

  /**
   * Name of the theme applied to this composition.
   * @example "ocean", "dark", "corporate"
   */
  themeName: string;

  /**
   * Name of the layout variant used.
   * @example "centered", "split", "editorial"
   */
  layoutName: string;
}

/**
 * Default props for a composition.
 *
 * These props are passed to the composition component when rendered.
 * They include the theme configuration, layout selection, and content data.
 */
export interface CompositionDefaultProps {
  /**
   * The theme to apply to the composition.
   */
  theme: Theme;

  /**
   * The layout variant to render.
   */
  layout: string;

  /**
   * The specification data for the composition content.
   */
  spec: Record<string, unknown>;

  /**
   * Optional brand kit for custom color overrides.
   */
  brandKit?: BrandKit;
}

/**
 * Props interface for composition components.
 *
 * This extends CompositionDefaultProps with any additional
 * props that may be passed at render time.
 */
export interface CompositionProps extends CompositionDefaultProps {
  /**
   * Additional props can be passed through.
   */
  [key: string]: unknown;
}

